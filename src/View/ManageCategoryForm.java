///////////////////////////////////// click to table to delete row
    private void tblFoodMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblFoodMouseClicked
        // TODO add your handling code here:
      try {
        int index = tblFood.getSelectedRow();
        TableModel model = tblFood.getModel();
        
        // Giả sử ID là kiểu String, điều chỉnh nếu là kiểu khác
        String id = model.getValueAt(index, 0).toString(); // Đảm bảo chỉ số cột khớp với cột ID

        // Hiển thị hộp thoại xác nhận
        int a = JOptionPane.showConfirmDialog(rootPane, "Bạn có muốn xóa ID: " + id + "?", "Xác nhận xóa", JOptionPane.YES_NO_OPTION);

        if (a == JOptionPane.YES_OPTION) {
            // Gọi phương thức xóa với ID đúng
            new DAO.ConnectionSQL().delete(id);
            JOptionPane.showMessageDialog(rootPane, "Delele complete.");
            
            // Tùy chọn: làm mới mô hình bảng sau khi xóa
            
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(rootPane, "Lỗi: " + e.getMessage());
    }
        
    }//GEN-LAST:event_tblFoodMouseClicked
//////////////////////////// display food method

    public void display() {
        for (Food food : foodList) {
            model.addRow(new Object[]{
                food.getId(), food.getCategory(), food.getFood(), food.getPrice()
            });
        }
    }

