////////////////// button add food
    private void btAddFoodActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAddFoodActionPerformed
        // TODO add your handling code here:
        try {
            Food food = new Food();
            
            String category = (String) cbCategory.getSelectedItem();
            String foodName = txtFood.getText();
            float price = Float.parseFloat(txtPrice.getText());
            food.setId(i);
            food.setCategory(category);
            food.setFood(foodName);
            food.setPrice(price);
            model.addRow(new Object[]{
                i++, food.getCategory(), food.getFood(), food.getPrice()
            });
            if (new DAO.ConnectionSQL().addFoodToSQL(food)) {
                JOptionPane.showMessageDialog(rootPane, "Add Success.");
            } else {
                JOptionPane.showMessageDialog(rootPane, "CANNOT Add !");
            }
            foodList.add(food);
        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_btAddFoodActionPerformed
